func startTestHTTPS(handler http.Handler, certificate []byte, privateKey []byte) *httptest.Server {
	ts := httptest.NewUnstartedServer(handler)
	cert, _ := tls.X509KeyPair(certificate, privateKey)
	existingConfig := ts.TLS
	if existingConfig != nil {
		ts.TLS = existingConfig.Clone()
	} else {
		ts.TLS = new(tls.Config)
	}
	if ts.TLS.NextProtos == nil {
		nextProtos := []string{"http/1.1"}
		if ts.EnableHTTP2 {
			nextProtos = []string{"h2"}
		}
		ts.TLS.NextProtos = nextProtos
	}

	ts.TLS.Certificates = []tls.Certificate{cert}
	ts.StartTLS()
	return ts
}
